    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
          try{
            String val1=name.getText(); 
            String val2=mno.getText();
            String val3=ano.getText();
            String val4=eid.getText();
            String val5=Password.getText();
            String ma= (String) maker.getSelectedItem();
            String mo= (String) model.getSelectedItem();
            String val6=ma;
            String val7=mo;
            String val8=evno.getText();
            String sql="UPDATE userinfo SET  uname='"+val1+"', mobile='"+val2+"', aid='"+val3+
                    "', email='"+val4+"', pass='"+val5+"'"
                    + ", make='"+val6+"', model='"+val7+"', evno='"+val8+"' WHERE mobile='"+val2+"' ";
            pst =conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null,"Data is Uptdated Successfuly...");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        finally{
            try{
                rs.close();
                pst.close();
                conn.close();
            }catch(Exception e){
            }
        }
    }//GEN-LAST:event_updateButtonActionPerformed

