    private void mnoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mnoKeyReleased
        // TODO add your handling code here:
        try{
            String sql="select * from userinfo where mobile = ?";

            pst=conn.prepareStatement(sql);
            pst.setString(1, mno.getText());
            rs=pst.executeQuery();

            String add1=rs.getString("mobile");
            mno.setText(add1);

            String add2=rs.getString("uname");
            name.setText(add2);
            
            String add3=rs.getString("aid");
            ano.setText(add3);            
            
            String add4=rs.getString("email");
            eid.setText(add4);

            String add5=rs.getString("pass");
            Password.setText(add5);      
            
            String add6=rs.getString("make");
            maker.setSelectedItem(add6);
            
            String add7=rs.getString("model");
            model.setSelectedItem(add7);
            
            String add8=rs.getString("evno");
            evno.setText(add8);
            
                 
        }catch(Exception e){

        }
        finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception e){

            }
        }
    }//GEN-LAST:event_mnoKeyReleased

    private void makerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_makerActionPerformed
        // TODO add your handling code here:
        try{
            if (maker.getSelectedItem().equals("TATA Motors")){
                model.removeAllItems();
                model.addItem("E-Tigor");
                model.addItem("Nexon");
                model.setSelectedItem(null);
            }
            else
            if (maker.getSelectedItem().equals("Mahindra")){
                model.removeAllItems();
                model.addItem("E-Verito");
                model.addItem("e20 Plus 8");
                model.setSelectedItem(null);
            }
            else
            if (maker.getSelectedItem().equals("Hyundai")){
                model.removeAllItems();
                model.addItem("Kona");
                model.setSelectedItem(null);
            }
            else
            if(maker.getSelectedItem().equals("MG Motors")){
                model.removeAllItems();
                model.addItem("ZS EV");
                model.setSelectedItem(null);
            }
            else
            if(maker.getSelectedItem().equals("Jaguar")){
                model.removeAllItems();
                model.addItem("I-PACE");
                model.setSelectedItem(null);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_makerActionPerformed

